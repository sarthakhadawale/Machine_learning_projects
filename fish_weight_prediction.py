from google.colab import files#Here we can upload file
data=files.upload()

import pandas as pd
fish=pd.read_csv("Fish[1].csv")

fish.info()#here we can check info

fish.drop("Species",axis=1,inplace=True)#Because species dtype=object that's why we can remove it

x=fish[["Length1","Length2","Length3","Height","Width"]]#here are input column

y=fish["Weight"]#here is output column

from sklearn.model_selection import train_test_split

x_train,x_test,y_train,y_test= train_test_split(x,y,test_size=0.30,random_state=25)#here we can split into 4 section

from sklearn.linear_model import LinearRegression
lm=LinearRegression()

lm.fit(x_train,y_train)#here we can train

prediction=lm.predict(x_test)#here we can test

import matplotlib.pyplot as plt
plt.scatter(y_test,y_test)

plt.scatter(y_test,prediction)

from sklearn import metrics

print("Mean Absolute Error",metrics.mean_absolute_error(y_test,prediction))#here we can find error

print("Mean squared Error",metrics.mean_squared_error(y_test,prediction)**0.5)